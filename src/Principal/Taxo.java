package Principal;

public class Taxo {
	private String nombre1, nombre2, nombre3, nombre4, depn1, depen2, depen3, depen4;

	public Taxo(String nombre1, String nombre2, String nombre3, String nombre4, String depn1, String depen2,
			String depen3, String depen4) {
		super();
		this.nombre1 = nombre1;
		this.nombre2 = nombre2;
		this.nombre3 = nombre3;
		this.nombre4 = nombre4;
		this.depn1 = depn1;
		this.depen2 = depen2;
		this.depen3 = depen3;
		this.depen4 = depen4;
	}

	public String getNombre1() {
		return nombre1;
	}

	public void setNombre1(String nombre1) {
		this.nombre1 = nombre1;
	}

	public String getNombre2() {
		return nombre2;
	}

	public void setNombre2(String nombre2) {
		this.nombre2 = nombre2;
	}

	public String getNombre3() {
		return nombre3;
	}

	public void setNombre3(String nombre3) {
		this.nombre3 = nombre3;
	}

	public String getNombre4() {
		return nombre4;
	}

	public void setNombre4(String nombre4) {
		this.nombre4 = nombre4;
	}

	public String getDepn1() {
		return depn1;
	}

	public void setDepn1(String depn1) {
		this.depn1 = depn1;
	}

	public String getDepen2() {
		return depen2;
	}

	public void setDepen2(String depen2) {
		this.depen2 = depen2;
	}

	public String getDepen3() {
		return depen3;
	}

	public void setDepen3(String depen3) {
		this.depen3 = depen3;
	}

	public String getDepen4() {
		return depen4;
	}

	public void setDepen4(String depen4) {
		this.depen4 = depen4;
	}

}
